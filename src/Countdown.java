public class Countdown {

    public static void main(String[] args) {
        
        System.out.println("Countdown to Launch: ");

        for(int i = 20; i >= 0; i--) {
            System.out.print(i +" "); 
        }

        System.out.println("Blast Off!");
        
        //Ir√° contar em ordem descrescente do 20 ao 0
    }
}
